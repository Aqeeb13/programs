package string;

public class printUpperCaseAndLowerCaseChar {

	public static void main(String[] args) {
		// upper case...................
		for(char ch='A'; ch<='Z';ch++) {
			System.out.print(ch+" ");
		}
		System.out.println(" ");
		
		//lower Case.....................
		for(char ch='a'; ch<='z';ch++) {
			System.out.print(ch+" ");
			
		}


	}

}
